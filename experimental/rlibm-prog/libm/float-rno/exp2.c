double rlibm_rno_exp2(float x) {
  float_x fx;
  fx.f = x;
  
  // Take care of special cases
  if ((fx.x & 0x7FFFFFFF) == 0) return 1.0;
  
  if (fx.x <= 876128826) {
    if (fx.x <= 867740218) return 1.0000000298023223876953125;
    return 1.0000000894069671630859375;
  }
  
  if (1124073472 <= fx.x && fx.x <= 3015223867) {
    if (fx.x < 0x80000000) {
      if (fx.x < 0x7F800000) return 3.40282361850336062550457001444955389952e+38;
      if (fx.x == 0x7F800000) return 1.0 / 0.0;
      return 0.0/0.0;
    }
    
    // negative counterpart
    if (fx.x <= 3006835259) return 0.99999998509883880615234375;
    
    return 0.99999995529651641845703125;
  }
  
  if (fx.x >= 3272998913) {
    if (fx.x == 0xFF800000) return 0.0;
    if (fx.x < 0xFF800000) return ldexp(1.0, -151);
    return 0.0/0.0;
  }
  
  // Perform range reduction
  double xp = x * 64;
  int N = (int)xp;
  int N2 = N % 64;
  if (N2 < 0) N2 += 64;
  int N1 = N - N2;
  
  int M = N1 / 64;
  int J = N2;
  double R = x - N * 0.015625;
  
  if (R == 0.0 && N2 == 0) {
    return ldexp(1.0, M);
  }

  double y = 0.0;
  
  if (R == -1.1414725333452224731445312500000000000000000000000000000000000000000000e-02) {
    y = 9.9211913117409722406847549791564233601093292236328125000000000000000000e-01;
  } else if (R == 2.9695758130401372909545898437500000000000000000000000000000000000000000e-03) {
    y = 1.0020604729652407005602299250313080847263336181640625000000000000000000e+00;
  } else if (R == 9.0990215539932250976562500000000000000000000000000000000000000000000000e-03) {
    y = 1.0063268918941958851576146116713061928749084472656250000000000000000000e+00;
  } else {
    y = 1.3612127772378225954580388545878122386056929826736450195312500000000000e-03;
    y *= R;
    y += 9.6182150117723481247411498884503089357167482376098632812500000000000000e-03;
    y *= R;
    y += 5.5504103270159553529250473502543172799050807952880859375000000000000000e-02;
    y *= R;
    y += 2.4022650695339856086185648109676549211144447326660156250000000000000000e-01;
    y *= R;
    y += 6.9314718055989188449927951296558603644371032714843750000000000000000000e-01;
    y *= R;
    y += 9.9999999999999988897769753748434595763683319091796875000000000000000000e-01;
  }

  // Perform output compensation
  return y * ldexp(exp2JBy64[J], M);
}
